    private void summaryEditorPaneHyperlinkUpdate(javax.swing.event.HyperlinkEvent evt) {//GEN-FIRST:event_summaryEditorPaneHyperlinkUpdate

        if( evt.getEventType() == HyperlinkEvent.EventType.ACTIVATED )
        {
            URL url = evt.getURL();
            Object source = evt.getSource();
            
            if( source==summaryEditorPane )
            {
                loadPage(url);
            }
        }
    }//GEN-LAST:event_summaryEditorPaneHyperlinkUpdate

