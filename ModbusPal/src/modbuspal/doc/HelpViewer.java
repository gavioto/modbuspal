    private void loadSummary()
    {
        try
        {
            URL url = getClass().getResource("summary.html");
            summaryEditorPane.setPage(url);
        }
        catch (Exception ex)
        {
            Logger.getLogger(HelpViewer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadPage(String filename)
    {
        URL url = getClass().getResource(filename);
        loadPage(url);
    }


    private void loadPage(URL url)
    {
        try {
            helpEditorPane.setPage(url);
        } catch (IOException ex) {
            Logger.getLogger(HelpViewer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

